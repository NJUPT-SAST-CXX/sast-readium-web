/**
 * Process text and replace emoji shortcodes with actual emojis
 * Supports :emoji_name: syntax
 */
export function processEmojis(text: string): string {
  return text.replace(/:([a-zA-Z0-9_+-]+):/g, (match, code) => {
    const emoji = emojiMap[code.toLowerCase()];
    return emoji || match;
  });
}

/**
 * Get emoji by shortcode
 */
export function getEmoji(code: string): string | undefined {
  return emojiMap[code.toLowerCase()];
}

/**
 * Search emojis by partial name
 */
export function searchEmojis(
  query: string,
  limit: number = 10
): Array<{ code: string; emoji: string }> {
  const lowerQuery = query.toLowerCase();
  const results: Array<{ code: string; emoji: string }> = [];

  for (const [code, emoji] of Object.entries(emojiMap)) {
    if (code.includes(lowerQuery)) {
      results.push({ code, emoji });
      if (results.length >= limit) break;
    }
  }

  return results;
}
